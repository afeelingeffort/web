
public class incdec_operator_5 {

	public static void main(String[] args) {
		int cnt=10;
		
		System.out.println(cnt++);	//10
		System.out.println(--cnt);	//10
		System.out.println(cnt--);	//10
		System.out.println(cnt--);	//9
		System.out.println(cnt++);	//8
		System.out.println(++cnt);	//10
		System.out.println(cnt--);	//10
		System.out.println(--cnt);	//8
		System.out.println(cnt++);	//8
		System.out.println(--cnt);	//8
		System.out.println(cnt++);	//8
	}

}
